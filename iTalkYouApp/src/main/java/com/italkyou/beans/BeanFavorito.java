package com.italkyou.beans;

public class BeanFavorito {
	
	private String ID;
	private BeanContact contacto;
	private String anexo;
	
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public BeanContact getContacto() {
		return contacto;
	}
	public void setContacto(BeanContact contacto) {
		this.contacto = contacto;
	}
	public String getAnexo() {
		return anexo;
	}
	public void setAnexo(String anexo) {
		this.anexo = anexo;
	}
	
	

}
