package com.italkyou.beans.entradas;

public class EntradaRedireccionarAnexo {
	
	private String idUsuario;
	private String idPais;
	private String telefono;
	private String fechaIni;
	private String fechaFin;
	private String flagActivo;
	private String flagPermanente;
	
	
	public String getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getIdPais() {
		return idPais;
	}
	public void setIdPais(String idPais) {
		this.idPais = idPais;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getFechaIni() {
		return fechaIni;
	}
	public void setFechaIni(String fechaIni) {
		this.fechaIni = fechaIni;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}

	public String getFlagActivo() {
		return flagActivo;
	}

	public void setFlagActivo(String flagActivo) {
		this.flagActivo = flagActivo;
	}

	public String getFlagPermanente() {
		return flagPermanente;
	}

	public void setFlagPermanente(String flagPermanente) {
		this.flagPermanente = flagPermanente;
	}
}
